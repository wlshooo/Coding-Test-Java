package codeup_basic;

import java.util.Scanner;

public class q1071 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        while (true) {
            int i = sc.nextInt();
            if (i != 0) {
                System.out.println(i);
            }
            else {
                break;
            }
        }
    }
}
